--[[
    Operadores de comparacao retornam um valor BOOLEAN (true, false)
    Maior que       >
    Menor que       <
    Maior ou igual  >=
    Menor ou igual  <=
    Igualdade       ==
]]

print(2 > 3)
--O retorno deste comando sera FALSE

print(2 < 3)
--O retorno deste comando será TRUE

print(2 <= 3)
--O retorno deste comando será TRUE

print(2 >= 3)
--O retorno deste comando sera FALSE